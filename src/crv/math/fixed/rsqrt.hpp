/// \file
/// \brief fixed point fast rsqrt
///
/// # Definition
///
/// This module solves `y = 1/sqrt(x)` over the domain `x > 0` using Newton-Raphson. We define a function, `f(y)`, with
/// the same roots as y, start with an initial guess near the solution, then iterate using the recurrence relation:
///
///     `y_{n+1} = y_n - f(y_n)/f'(y_n)`
///
/// Each step finds the line tangent to `f(y_n)`, finds the horizontal intercept of that tangent, then repeats with
/// `y_{n+1}` set to that intercept. With a good initial guess for `y_0`, this converges quadratically to the root of
/// `f(y)`.
///
/// # Derivation
///
/// In the case of `y = 1/sqrt(x)`, we choose `f(y) = y^-2 - x`:
///
///     ```
///        y = 1/sqrt(x)    [given]
///      y^2 = 1/x          [square both sides]
///     xy^2 = 1            [multiply both sides by x]
///        x = y^-2         [divide both sides by y^2]
///        0 = y^-2 - x     [find root]
///     ```
///
/// There are other choices, but this has an important property. Given `f'(y) = -2y^-3`:
///
///     ```
///     y_{n+1} = y_n - f(y_n)/f'(y_n)             [original]
///             = y_n - (y_n^-2 - x)/(-2y_n^-3)    [substitute functions]
///             = y_n + y_n^3(y_n^-2 - x)/2        [multiply right by -y_n^3/-y_n^3]
///             = y_n + y_n(1 - xy_n^2)/2          [distribute y_n^2]
///             = y_n(1 + (1 - xy_n^2)/2)          [factor out common y_n]
///             = y_n(3 - xy_n^2)/2                [combine constants]
///     ```
///
/// This form allows calculating rsqrt using only multiplication, subtraction, and a shift.
///
/// # Convergence
///
/// We choose to characterize the error multiplicatively using `(1/sqrt(x))(1 - e_n)`. There are other choices, but this
/// makes the algebra straightforward:
///
///     ```
///                 y_n = (1/sqrt(x))(1 - e_n)                       [definition]
///               y_n^2 = (1/x)(1 - e_n)^2                           [square both sides]
///             x*y_n^2 = (1 - e_n)^2                                [multiply by x]
///                     = 1 - 2e_n + e_n^2                           [expand]
///         3 - x*y_n^2 = 2 + 2e_n - e_n^2                           [subtract from 3]
///     (3 - x*y_n^2)/2 = 1 + e_n - e_n^2/2                          [divide by 2]
///             y_{n+1} = y_n(1 + e_n - e_n^2/2)                     [NR recurrence]
///                     = (1/sqrt(x))(1 - e_n)(1 + e_n - e_n^2/2)    [sub y_n]
///     ```
/// Expanding (1 - e_n)(1 + e_n - e_n^2/2):
///
///     ```
///     (1 - e_n)(1 + e_n - e_n^2/2) = (1 - e_n)1 + (1 - e_n)e_n - (1 - e_n)e_n^2/2    [distribute (1 - e_n)]
///                                  = 1 + e_n - e_n^2/2 - e_n - e_n^2 + e_n^3/2       [distribute (1 - e_n)]
///                                  = 1 - 3e_n^2/2 + e_n^3/2                          [combine like terms]
///                                  = 1 - e_n^2(3 - e_n)/2                            [factor out common e_n^2]
///     ```
///
/// So `y_{n+1} = (1/sqrt(x))(1 - e_n^2(3 - e_n)/2)`:
///
///     ```
///         y_{n+1} = (1/sqrt(x))(1 - e_{n+1})             [definition]
///         y_{n+1} = (1/sqrt(x))(1 - e_n^2(3 - e_n)/2)    [derived]
///
///     1 - e_{n+1} = 1 - e_n^2(3 - e_n)/2    [given]
///        -e_{n+1} = -e_n^2(3 - e_n)/2       [subtract 1 from both sides]
///         e_{n+1} = e_n^2(3 - e_n)/2        [negate]
///     ```
///
/// For small `|e_n|`, `(3 - e_n) ~= 3`, so `e_{n+1} ~= 1.5e_n^2`. This is why a good initial guess converges
/// quadratically.
///
/// # Initial Guess
///
/// The initial guess is found using a quadratic minimax approximation of `1/sqrt(x)` using Horner's method:
///
///     `C0 + -C1*x + C2*x^2 = C0 - x*(C1 - x*C2)`.
///
/// The coefficients are generated by Sollya over the domain [0.5, 1.0) in unsigned Q2.62.
///
/// Using a quadratic approximation balances Horner iterations against Newton-Raphson iterations. Each NR iteration uses
/// 3 multiplies. Horner iterations use 1. For the same precision, a -log2/2 approximation requires 6 NR iterations.
/// Linear requires 4. Quadratic requires 3. Cubic also requires 3, but is more expensive to evaluate than quadratic, so
/// we use quadratic.
///
/// Budget 3 narrowings per iteration, then add a constant slack of 2. Truncation error for iteration i is 3i + 2. With
/// the quadratic approximation, the worst case error becomes noise-limited after 3 iterations, dropping below the
/// fixed-point rounding floor:
///
///     iterations | approximation error (ulps) | truncation error(ulps) | max error (ulps)
///         1      |     101527141262302        |            5           | 101527141262302
///         2      |          3352684267        |            8           |      3352684267
///         3      |                   4        |           11           |              11
///         4      |                   1        |           14           |              14
///
/// # Newton-Raphson
///
/// NR iteration is performed in the reduced range [0.5, 1.0). Range reduction uses clz to normalize the input to this
/// range. Restoration rescales the result back to the original range.
///
